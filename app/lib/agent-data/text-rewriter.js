/**
 * 선택된 톤과 용도에 맞는 시스템 프롬프트 세그먼트를 빌드한다.
 */
export function buildTextRewriterContext({ toneId, purposeId }) {
  const tone = TONE_DEFINITIONS.find((t) => t.id === toneId);
  const purpose = PURPOSE_TEMPLATES.find((p) => p.id === purposeId);

  const sections = [];

  if (tone) {
    sections.push(`[톤: ${tone.name}]`);
    sections.push(`설명: ${tone.description}`);
    sections.push(`특성:\n${tone.characteristics.map((c) => `- ${c}`).join('\n')}`);
    sections.push(`올바른 표현 예시:\n${tone.doList.map((d) => `✓ ${d}`).join('\n')}`);
    sections.push(`피해야 할 표현:\n${tone.dontList.map((d) => `✗ ${d}`).join('\n')}`);
    if (tone.examples.length > 0) {
      sections.push('변환 예시:');
      tone.examples.forEach((ex, i) => {
        sections.push(`  ${i + 1}) 원문: ${ex.before}`);
        sections.push(`     변환: ${ex.after}`);
      });
    }
  }

  if (purpose) {
    sections.push(`\n[용도: ${purpose.name}]`);
    sections.push(`구조:`);
    sections.push(`  서두: ${purpose.structure.opening}`);
    sections.push(`  본문: ${purpose.structure.body}`);
    sections.push(`  마무리: ${purpose.structure.closing}`);
    sections.push(`템플릿:\n${purpose.template}`);
    sections.push(`팁:\n${purpose.tips.map((t) => `- ${t}`).join('\n')}`);
  }

  // 스타일 가이드 (한국어 기본)
  const sg = STYLE_GUIDES.korean;
  sections.push(`\n[${sg.title}]`);
  sections.push(`존칭: ${sg.honorifics.rules.slice(0, 3).join(' / ')}`);
  sections.push(`접속사: ${sg.conjunctions.rules.slice(0, 4).join(' / ')}`);
  sections.push(`단락: ${sg.paragraphs.rules.slice(0, 3).join(' / ')}`);

  // 관련 변환 예시 추가
  if (tone) {
    const relatedExamples = TRANSFORMATION_EXAMPLES.filter(
      (ex) => ex.targetTone === toneId
    ).slice(0, 3);
    if (relatedExamples.length > 0) {
      sections.push('\n[참고 변환 예시]');
      relatedExamples.forEach((ex) => {
        sections.push(`  "${ex.original}" → "${ex.transformed}"`);
      });
    }
  }

  return sections.join('\n');
}
